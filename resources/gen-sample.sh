#!/usr/bin/env bash
# gen-sample.sh — Generate a sample project for testing tasklin features.
#
# Creates (or recreates) a `sample/` directory at the repo root containing:
#   - An initialised git repository
#   - Dummy source files to simulate a real project
#   - A .todo/ folder with 1000+ tickets spread across statuses
#
# Usage:
#   bash resources/gen-sample.sh          # run from repo root
#   bash resources/gen-sample.sh --clean  # wipe and regenerate

set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
REPO_ROOT="$(cd "$SCRIPT_DIR/.." && pwd)"
SAMPLE_DIR="$REPO_ROOT/sample"

# ---------------------------------------------------------------------------
# Flags
# ---------------------------------------------------------------------------
CLEAN=false
for arg in "$@"; do
  case "$arg" in --clean|-c) CLEAN=true ;; esac
done

if $CLEAN && [ -d "$SAMPLE_DIR" ]; then
  echo "→ Removing existing sample/ directory..."
  rm -rf "$SAMPLE_DIR"
fi

if [ -d "$SAMPLE_DIR/.todo" ]; then
  echo "✓ sample/ already exists. Pass --clean to regenerate."
  exit 0
fi

echo "→ Creating sample project at $SAMPLE_DIR"
mkdir -p "$SAMPLE_DIR"

# ---------------------------------------------------------------------------
# Git repo
# ---------------------------------------------------------------------------
echo "→ Initialising git repository..."
cd "$SAMPLE_DIR"
git init -q
git config user.email "sample@tasklin.dev"
git config user.name "Tasklin Sample"

# ---------------------------------------------------------------------------
# Dummy project files (simulates a small Go service)
# ---------------------------------------------------------------------------
echo "→ Writing dummy project files..."

mkdir -p src/{auth,api,db,worker} tests docs

cat > src/main.go <<'GO'
package main

import "fmt"

func main() {
	fmt.Println("sample service starting...")
}
GO

cat > src/auth/auth.go <<'GO'
package auth

// Token represents an authentication token.
type Token struct {
	Value  string
	UserID int
}

func Validate(t Token) bool {
	return t.Value != "" && t.UserID > 0
}
GO

cat > src/api/router.go <<'GO'
package api

import "net/http"

func NewRouter() *http.ServeMux {
	mux := http.NewServeMux()
	mux.HandleFunc("/health", healthHandler)
	return mux
}

func healthHandler(w http.ResponseWriter, r *http.Request) {
	w.WriteHeader(http.StatusOK)
}
GO

cat > src/db/db.go <<'GO'
package db

import "fmt"

type Config struct {
	DSN string
}

func Connect(cfg Config) error {
	if cfg.DSN == "" {
		return fmt.Errorf("DSN is required")
	}
	return nil
}
GO

cat > src/worker/worker.go <<'GO'
package worker

import "time"

type Worker struct {
	interval time.Duration
	stop     chan struct{}
}

func New(interval time.Duration) *Worker {
	return &Worker{interval: interval, stop: make(chan struct{})}
}

func (w *Worker) Start() {
	for {
		select {
		case <-w.stop:
			return
		case <-time.After(w.interval):
			// process jobs
		}
	}
}

func (w *Worker) Stop() { close(w.stop) }
GO

cat > tests/auth_test.go <<'GO'
package tests

import (
	"testing"
)

func TestPlaceholder(t *testing.T) {
	t.Log("placeholder test")
}
GO

cat > docs/architecture.md <<'MD'
# Architecture

